package com.example.object;

import java.io.Serializable;

public class Contacto implements Serializable  {
	private Integer idContacto;
	private String numeroTelefono;
	private String nombre;
	private String numero;
	private String correo;
	private Integer estado;
	private Integer alertaSMS;
	private Integer alertaGPS;
	private Integer alertaCorreo;
	public Integer getIdContacto() {
		return idContacto;
	}
	public void setIdContacto(Integer idContacto) {
		this.idContacto = idContacto;
	}
	public String getNumeroTelefono() {
		return numeroTelefono;
	}
	public void setNumeroTelefono(String numeroTelefono) {
		this.numeroTelefono = numeroTelefono;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	public String getNumero() {
		return numero;
	}
	public void setNumero(String numero) {
		this.numero = numero;
	}
	public String getCorreo() {
		return correo;
	}
	public void setCorreo(String correo) {
		this.correo = correo;
	}
	public Integer getEstado() {
		return estado;
	}
	public void setEstado(Integer estado) {
		this.estado = estado;
	}
	public Integer getAlertaSMS() {
		return alertaSMS;
	}
	public void setAlertaSMS(Integer alertaSMS) {
		this.alertaSMS = alertaSMS;
	}
	public Integer getAlertaGPS() {
		return alertaGPS;
	}
	public void setAlertaGPS(Integer alertaGPS) {
		this.alertaGPS = alertaGPS;
	}
	public Integer getAlertaCorreo() {
		return alertaCorreo;
	}
	public void setAlertaCorreo(Integer alertaCorreo) {
		this.alertaCorreo = alertaCorreo;
	}
	
	
}
